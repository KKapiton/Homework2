package Pinocchio;

public class Pinocchio {
    public static void main(String[] args) {
        final int pinocchioApple = 10;
        final int theftBarabas = 3;
        final int remeinsPinocchio = pinocchioApple - theftBarabas;

        System.out.println("Изначально яблок у Пиннокио = " + pinocchioApple);
        System.out.println("Украл Карабас Барабас = " + theftBarabas);
        System.out.println("У Пиннокио осталось яблок после кражи = " + remeinsPinocchio);
    }
}